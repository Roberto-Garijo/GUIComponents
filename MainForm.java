    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        LocalDate birth = LocalDate.parse(txtBirthDate.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd"));
        
        String gender = "";
        
        if(radMale.isSelected())
            gender = "Male";
        else if(radFemale.isSelected())
            gender = "Female";
        
        User user = new User(txtId.getText(), txtFirstName.getText(), txtLastName.getText(), gender, birth, chkAlive.isSelected());
        users.add(user);
        
        //txtaText.setText(txtaText.getText() + user.toString());
        txtaText.append(txtaText.getText() + user.toString());
    }//GEN-LAST:event_btnInsertActionPerformed

