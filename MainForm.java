    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            /*File file = new File("datos.txt");
            if (!file.exists())
                file.createNewFile();
            
            FileWriter fw = new FileWriter(file.getAbsoluteFile(), true);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(txtaText.getText());*/
            
            BufferedWriter bw = new BufferedWriter(new FileWriter("datos.txt"));
            
            for (User u: users) {
                String userString = u.getId() + "," + u.getLastName() + "," + u.getFirstName() + ","
                    + u.getGender() + "," + u.getBirthDate().getYear() + "-" + u.getBirthDate().getMonthValue() 
                    + "-" + u.getBirthDate().getDayOfMonth() + "," + (u.isIsAlive() ? "Alive" : "Dead")
                    + System.lineSeparator();
                
                bw.append(userString);
            }
            
            bw.close();
        }
        catch (FileNotFoundException fnfe){
            System.err.println("Error en la creacion del archivo.");
        }
        catch (IOException ioe) {
            System.err.println("Error en la escitura del archivo.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        // TODO add your handling code here:
        try {
            users.clear();
            BufferedReader br = new BufferedReader(new FileReader("datos.txt"));
            
            String line = br.readLine();
            while (line != null) {
                String[] fields = line.split(",");
                User user = new User(fields[0], fields[2], fields[1],
                    fields[3], LocalDate.parse(fields[4]), fields[5].equals("Alive"));
                //txtaText.setText(line);
                
                users.add(user);
                line = br.readLine();
            }
            //System.out.println();
        }
        catch (FileNotFoundException fnfe) {
            System.err.println("Error en la busqueda del archivo.");
        }
        catch (IOException ioe) {
            System.err.println("Error en la lectura del archivo.");
        }
        
        for (User u: users)
            txtaText.append(u.toString());
        
        // Es lo mismo que el for de arriba
        /*for (int i = 0; i < users.size(); i++)
            txtaText.append(users.get(i).toString());*/
    }//GEN-LAST:event_btnLoadActionPerformed

